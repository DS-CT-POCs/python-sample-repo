"""
Simple math utilities for sample RAG scenarios.
"""


def add(a: float, b: float) -> float:
    """Add two numbers."""
    return a + b


def multiply(a: float, b: float) -> float:
    """Multiply two numbers."""
    return a * b


def square(n: float) -> float:
    """Return n * n."""
    return multiply(n, n)
